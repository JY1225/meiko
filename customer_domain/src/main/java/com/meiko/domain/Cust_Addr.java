package com.meiko.domain;

public class Cust_Addr {

	private int recid;
	private String cust_code;
	private String cust_name;
	private String cust_shipping_addr;
	public int getRecid() {
		return recid;
	}
	public void setRecid(int recid) {
		this.recid = recid;
	}
	public String getCust_code() {
		return cust_code;
	}
	public void setCust_code(String cust_code) {
		this.cust_code = cust_code;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_shipping_addr() {
		return cust_shipping_addr;
	}
	public void setCust_shipping_addr(String cust_shipping_addr) {
		this.cust_shipping_addr = cust_shipping_addr;
	}
	
}

package com.meiko.domain;

public class Parameter {

	private String flag;
	private long id;
	private long rkey;
	

	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getRkey() {
		return rkey;
	}
	public void setRkey(long rkey) {
		this.rkey = rkey;
	}
	
	
}
